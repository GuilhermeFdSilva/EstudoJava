package fundamentos.operadores;

public class Arentimeticos {

	public static void main(String[] args) {
		
		System.out.println(3 +2);
		
		double x = 2.5;
		double y = 7.5;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		
		int a = 2;
		int b = 8;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a / (double) b);
		
		
		
	}
}
	